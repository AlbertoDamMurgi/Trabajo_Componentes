public class Barra {

    private int color;
    private int valor;

    public Barra(int color, int valor) {
        this.color = color;
        this.valor = valor;
    }

    public int getColor() {
        return color;
    }

    public void setColor(int color) {
        this.color = color;
    }

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }
}
